    Dear Wesleyanites
    <br>
    <pre>
    If you know of a clarinettist present at Wesleyan in 1969
    who headed a dixieland/ragtime band,
    please tell me who it was.
    He made a crazy leap of faith (or poor judgment, if that's different).
    He invited me to a rehearsal despite at least 4 good reasons not to,
    and only one on the other side:  my passion to return to playing music.
    
    This decision brought the tuba into my life 
    and enriched it in indescribable ways.
    On my late-life quest to thank people who have helped through the decades,
    it would be so satisfying to give this person the deepest expression of gratitude.
    
    I only know that Charlie Ziff was in the band, and I think Lee Henderson.
    Further, to play a party at an English professor's house,
    we carried an upright piano over a chasm across a wooden bridge with twisty stairs in the middle.
    That was December 1969.
    </pre>
    
